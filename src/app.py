# OAuth2 scheme
oauth2_scheme = OAuth2PasswordBearer(tokenUrl="token")

# Mock user database
users_db = {
    "teacher1": {
        "username": "teacher1",
        "password": "password1",
        "role": "teacher"
    },
    "student1": {
        "username": "student1",
        "password": "password1",
        "role": "student"
    }
}

def authenticate_user(username: str, password: str):
    user = users_db.get(username)
    if not user or user["password"] != password:
        return None
    return user

def get_current_user(token: str = Depends(oauth2_scheme)):
    # Mock token decoding
    user = users_db.get(token)
    if not user:
        raise HTTPException(status_code=401, detail="Invalid authentication credentials")
    return user

def get_current_active_user(current_user: dict = Depends(get_current_user)):
    if current_user.get("role") not in ["teacher", "student"]:
        raise HTTPException(status_code=403, detail="Inactive user")
    return current_user


@app.get("/")
def root():
    return RedirectResponse(url="/static/index.html")


@app.post("/token")
def login(form_data: OAuth2PasswordRequestForm = Depends()):
    user = authenticate_user(form_data.username, form_data.password)
    if not user:
        raise HTTPException(status_code=401, detail="Incorrect username or password")
    return {"access_token": user["username"], "token_type": "bearer"}


@app.get("/activities", dependencies=[Depends(get_current_active_user)])
def get_activities():
    return activities


@app.post("/activities/{activity_name}/signup", dependencies=[Depends(get_current_active_user)])
def signup_for_activity(activity_name: str, email: str):
    """Sign up a student for an activity"""
    # Validate activity exists
    if activity_name not in activities:
        raise HTTPException(status_code=404, detail="Activity not found")

    # Get the specific activity
    activity = activities[activity_name]

    # Validate student is not already signed up
    if email in activity["participants"]:
        raise HTTPException(
            status_code=400,
            detail="Student is already signed up"
        )

    # Add student
    activity["participants"].append(email)
    return {"message": f"Signed up {email} for {activity_name}"}


@app.delete("/activities/{activity_name}/unregister", dependencies=[Depends(get_current_active_user)])
def unregister_from_activity(activity_name: str, email: str):
    """Unregister a student from an activity"""
    # Validate activity exists
    if activity_name not in activities:
        raise HTTPException(status_code=404, detail="Activity not found")

    # Get the specific activity
    activity = activities[activity_name]

    # Validate student is signed up
    if email not in activity["participants"]:
        raise HTTPException(
            status_code=400,
            detail="Student is not signed up for this activity"
        )

    # Remove student
    activity["participants"].remove(email)
    return {"message": f"Unregistered {email} from {activity_name}"}
